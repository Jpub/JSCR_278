// 숫자형 변수의 덧셈
let number1 = 10;
let number2 = 20;
let sum = number1 + number2;
console.log(sum); // 결과：30

// 문자열 변수의 결합
let firstName= '브렌던';
let familyName  = '아이크';
let fullName = firstName + familyName ;
console.log(fullName); // 결과：'브렌던아이크'

// 변수를 다른 변수에 대입
let value1 = 100;
let value2 = value1;
console.log(value2); // 결과: 100 (value1과 같은 값)
